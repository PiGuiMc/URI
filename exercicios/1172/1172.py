
x = 1
j = 0
while x != 0:
    
    for i in range(j < 10):
        n = int(input())
        
        if n <= 0:
            print('X[{:.0f}]'.format(j),'= {:.0F}'.format(x))
        else:
            print('X[{:.0f}]'.format(j),'= {:.0F}'.format(n))
       
    j += 1
    if j == 10:
        x = 0
